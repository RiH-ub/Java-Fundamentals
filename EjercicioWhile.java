public class EjercicioWhile {
    
    public static void main(String[] args) {    
        int i = 0; // Variable de inicializacion, inicia del numero 0 
        while (i <= 9) { // Condicion, sirve para repetir
            System.out.println(i); // BLoque de sentencias
            i++; // Variable de actualizacion
            

        }    

    }
}